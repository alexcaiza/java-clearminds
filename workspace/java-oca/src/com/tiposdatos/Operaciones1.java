package com.tiposdatos;

public class Operaciones1 {
    
    public static void main(String[] args) {
        int a = 5/2;
        System.out.println("a: " + a);
        
        double d = 5/2;
        System.out.println("d: " + d);
        
        double f = 5/2;
        System.out.println("f: " + f);
        
        double x = (double) 5 / (float)2;
        System.out.println("x: " + x);
    }

}
