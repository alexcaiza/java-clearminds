package com.tiposdatos;

public class Autoboxing {
    
    //Autoboxing es la conversión automática que el compilador de Java hace que entre los tipos primitivos 
    //y sus clases de objetos(conocidos como wrappers).
    
    //Por ejemplo, la conversión de un int a un Integer, un double con un Double, y así sucesivamente.
    
    //Si la conversión es viceversa esto se llama Unboxing.
	
	public static void main(String[] args) {
		
	    Byte b = 10;
	    Short s  =10;
	    Integer a = 10;
	    
	    long l2 = 10;
	    Long l = l2;
	    
	    Float f = 10.0f;	    

	    //Double d = f;
	    
	    
	}

}
